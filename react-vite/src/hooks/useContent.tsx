/**
 * @param {enum} defLangKey - ['ru']
 */

export default function useContent(defLangKey: 'ru', blockName?: keyof typeof content[typeof defLangKey]) {

  const [lang, setLang] = useState(defLangKey);
  const res = content[lang];
  return [blockName ? res[blockName] : res, setLang, lang];

    
}


